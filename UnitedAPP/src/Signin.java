    public void inputUserDetails(String username) {
        Connection DBConn = null;
        int executeUpdateVal;
        try {
            String sourceURL = "jdbc:mysql://localhost:3306/users";
            DBConn = DriverManager.getConnection(sourceURL, "root", "");
            String SQL = String.format("INSERT INTO userlog(username,activity) VALUES ('%s', '%s')", username, "Signed in");
            System.out.println(SQL);
            PreparedStatement stmt = DBConn.prepareStatement(SQL);
            executeUpdateVal = stmt.executeUpdate();
            System.out.println(executeUpdateVal);
        } catch (Exception e) {
            System.out.println(e);
            jLabel1.setText("Error inserting value into userlog table");

        }
    }

