        Boolean connectError = false;       // Error flag
        Connection DBConn = null;           // MySQL connection handle    
        String errString = null;
        ResultSet res = null;               // SQL query result set pointer
        Statement s = null;                 // SQL statement pointer
        Boolean orderBlank = false;         // False: order string is not blank
        String SQLStatement;
        final String userName = jTextField1.getText();
        String password = jPasswordField1.getText();
        if (!userName.trim().isEmpty() && !password.trim().isEmpty()) {
            try {

                String sourceURL = "jdbc:mysql://localhost:3306/users";
                DBConn = DriverManager.getConnection(sourceURL, "root", "");
                String SQL = String.format(" SELECT username,department FROM userdetails WHERE username = '%s'  and password='%s' ", userName, password);
                System.out.println(SQL);
                PreparedStatement stmt = DBConn.prepareStatement(SQL);
                ResultSet rs = stmt.executeQuery();
                String department = "";

                if (rs.next()) {
                    department = rs.getString("department");
                    inputUserDetails(userName);
                    if (department.equals("IT Department")) {
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                dispose();
                                new Inventory(userName).setVisible(true);
                            }
                        });
                    } else if (department.equals("Shipping Department")) {
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                dispose();
                                new Shipping(userName).setVisible(true);
                            }
                        });
                    } else if (department.equals("Orders Department")) {
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                dispose();
                                new Order(userName).setVisible(true);
                            }
                        });
                    }
                } else {
//                    jTextArea1.setText("User does not exist.. ");
                    jLabel1.setText("Login or password is incorrect");
                }
            } catch (Exception e) {
                jLabel1.setText("Error connecting to userlog database" + e);

                connectError = true;
            }
        } else {
            jLabel1.setText("Missing user credentials");
        }
